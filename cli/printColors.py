#   Allows for colors to be used when printing to the terminal.
#   Such use case would be using red for a warning
#   Created by Kyle Erwin - 19/07/2017


class PrintColors:
    HEADER = '\033[95m'
    OKBLUE = '\033[94m'
    OKGREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    ENDC = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'